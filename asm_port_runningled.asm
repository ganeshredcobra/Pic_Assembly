#INCLUDE P16F877A.INC
CBLOCK 0X20
RESULT
DATA1
DATA2
ENDC

ORG 00H
GOTO MAIN
ORG 20H
MAIN:
	BANKSEL TRISB
	CLRF TRISB
	BANKSEL PORTB
	CLRF PORTB
	CLRF DATA1
	CLRF DATA2
	CLRF RESULT
	MOVLW 01H
	MOVWF DATA2
	MOVLW 00H
	MOVWF DATA1	
	CALL LOOP
LOOP:
	ADDWF DATA2,W
	MOVWF RESULT
	MOVF RESULT,W
	MOVWF DATA2
	MOVF RESULT,W
	MOVWF PORTB
	GOTO LOOP
END