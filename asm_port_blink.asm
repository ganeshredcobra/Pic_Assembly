#INCLUDE P16F877A.INC
CBLOCK 0X20
DATA1
ENDC

ORG 00H
GOTO MAIN
ORG 20H

MAIN:
	BANKSEL TRISB
	CLRF TRISB
	BANKSEL PORTB
	CLRF PORTB
	MOVLW 0XFF
	MOVWF PORTB
	CALL DELAY
	MOVLW 0X00
	MOVWF PORTB
	CALL DELAY
	GOTO MAIN
DELAY:
	MOVLW 0XFF
	MOVWF DATA1
LABEL:
	DECFSZ DATA1,F
	GOTO LABEL
	RETURN
END